--Supporting Tulip
local dm=require "expansions.utility_dmtcg"
local scard,sid=dm.GetID()
function scard.initial_effect(c)
	dm.EnableCreatureAttribute(c)
	--get ability (power attacker)
	local e1=Effect.CreateEffect(c)
	e1:SetType(EFFECT_TYPE_SINGLE)
	e1:SetCode(DM_EFFECT_UPDATE_POWER)
	e1:SetProperty(EFFECT_FLAG_SINGLE_RANGE)
	e1:SetRange(DM_LOCATION_BATTLE)
	e1:SetCondition(dm.SelfAttackerCondition)
	e1:SetValue(4000)
	local e2=Effect.CreateEffect(c)
	e2:SetType(EFFECT_TYPE_FIELD+EFFECT_TYPE_GRANT)
	e2:SetRange(DM_LOCATION_BATTLE)
	e2:SetTargetRange(DM_LOCATION_BATTLE,DM_LOCATION_BATTLE)
	e2:SetTarget(aux.TargetBoolFunction(Card.IsDMRace,DM_RACE_ANGEL_COMMAND))
	e2:SetLabelObject(e1)
	c:RegisterEffect(e2)
	local e3=Effect.CreateEffect(c)
	e3:SetType(EFFECT_TYPE_FIELD)
	e3:SetCode(DM_EFFECT_POWER_ATTACKER)
	e3:SetRange(DM_LOCATION_BATTLE)
	e3:SetTargetRange(DM_LOCATION_BATTLE,DM_LOCATION_BATTLE)
	e3:SetTarget(aux.TargetBoolFunction(Card.IsDMRace,DM_RACE_ANGEL_COMMAND))
	c:RegisterEffect(e3)
end
scard.duel_masters_card=true
